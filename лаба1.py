def количество_кроликов(n, m):
    # Для n = 1 и n = 2 по условию результат равен 1 (одна пара кроликов)
    if n == 1 or n == 2:
        return 1

    # Создаем список размером n+1, где индекс соответствует номеру месяца,
    # а индекс 0 используем для вспомогательного значения (не представляет реальный месяц).
    кролики = [0] * (n + 1)
    кролики[0] = 1  # вспомогательное значение — нужно для вычитания в рекурренте
    кролики[1] = 1  # месяц 1: 1 пара кроликов
    кролики[2] = 1  # месяц 2: 1 пара кроликов

    # Для месяцев с 3-го и далее:
    for месяц in range(3, n + 1):
        if месяц <= m:
            # До того как начнут умирать кролики (месяц не превышает жизни m),
            # количество пар равно сумме в двух предыдущих месяцах
            кролики[месяц] = кролики[месяц - 1] + кролики[месяц - 2]
        else:
            # Когда кролики начинают умирать, вычитаем число пар,
            # которые появились в месяце, когда кролики, родившиеся тогда, умирают
            кролики[месяц] = кролики[месяц - 1] + кролики[месяц - 2] - кролики[месяц - m - 1]
    return кролики[n]


# Пример работы:
n = int(input("Введите число месяцев (n): "))
m = int(input("Введите срок жизни кроликов в месяцах (m): "))
результат = количество_кроликов(n, m)
print(f"Количество пар кроликов на {n}-й месяц: {результат}")
